<?php
declare(strict_types=1);

namespace Themis;

require_once __DIR__ . '/StrictErrorHandler.php';
require_once __DIR__ . '/Autoloader.php';

use Throwable;
use Themis\Combat\CombatService;
use Themis\System\DatabaseOperator;

header('Content-Type: application/json; charset=utf-8');
header('Cache-Control: no-store, no-cache, must-revalidate, max-age=0');
header('Pragma: no-cache');

if (session_status() !== PHP_SESSION_ACTIVE) {
	if (!session_start()) {
		http_response_code(500);
		echo json_encode(['ok' => false, 'error' => 'Session start failed']);
		exit;
	}
}

function combat_respond(int $status, array $payload): void
{
	http_response_code($status);
	echo json_encode($payload, JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES);
	exit;
}

function combat_current_player_uuid(): string
{
	if (!isset($_SESSION['player']) || !is_array($_SESSION['player'])) {
		return '';
	}
	return trim((string)($_SESSION['player']['player_uuid'] ?? ''));
}

function combat_get_json_body(): array
{
	$raw = file_get_contents('php://input');
	if (!is_string($raw) || trim($raw) === '') {
		return [];
	}
	$decoded = json_decode($raw, true);
	return is_array($decoded) ? $decoded : [];
}

function combat_is_test_mode(array $body): bool
{
	$flag = (string)($_GET['test_mode'] ?? $_POST['test_mode'] ?? ($body['test_mode'] ?? ''));
	return trim($flag) === '1';
}

function combat_normalize_uuid(string $value): string
{
	$uuid = strtolower(trim($value));
	if ($uuid === '') {
		return '';
	}
	if (!preg_match('/^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/', $uuid)) {
		return '';
	}
	return $uuid;
}

function combat_get_test_actor_uuid(array $body): ?string
{
	if (!combat_is_test_mode($body)) {
		return null;
	}
	$raw = (string)($_GET['test_actor_uuid'] ?? $_POST['test_actor_uuid'] ?? ($body['test_actor_uuid'] ?? ''));
	$uuid = combat_normalize_uuid($raw);
	return $uuid !== '' ? $uuid : null;
}

function combat_uuid_v4(): string
{
	$bytes = random_bytes(16);
	$bytes[6] = chr((ord($bytes[6]) & 0x0f) | 0x40);
	$bytes[8] = chr((ord($bytes[8]) & 0x3f) | 0x80);
	$hex = bin2hex($bytes);
	return sprintf(
		'%s-%s-%s-%s-%s',
		substr($hex, 0, 8),
		substr($hex, 8, 4),
		substr($hex, 12, 4),
		substr($hex, 16, 4),
		substr($hex, 20, 12)
	);
}

