{
	return json_encode([
		'@invis@' => $name,
		'Species:' => 'Test Duelist',
		'Mood:' => 'Focused',
		'Info:' => 'Autogenerated duel test character.',
		'Scent:' => 'None',
		'Currently:' => 'In testing mode.',
		'template' => 'character',
		'0' => $name,
	], JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES) ?: '{}';
}

